def run() -> None:
    db = SessionLocal()
    try:
        issues = {i.name: i for i in db.scalars(select(Issue)).all()}
        bills = {b.bill_no: b for b in db.scalars(select(Bill)).all()}
        n_new = n_skip = n_no_bill = 0

        for d in DRAFTS:
            issue = issues.get(d.issue)
            if issue is None:
                raise RuntimeError(f"쟁점 '{d.issue}' 없음 — app.seed 먼저 실행")

            exists = db.scalar(
                select(Question).where(
                    Question.issue_id == issue.id, Question.body == d.body
                )
            )
            if exists:
                n_skip += 1
                continue

            bill = bills.get(d.bill_no)
            if bill is None:
                n_no_bill += 1  # 의안 미적재(bills 잡 전) — bill_id 없이 초안만 등록

            db.add(
                Question(
                    issue_id=issue.id,
                    body=d.body,
                    agree_meaning=d.a_pro,  # 하위호환 요약
                    disagree_meaning=d.b_pro,
                    option_a_label=d.a_label,
                    option_a_pro=d.a_pro,
                    option_a_con=d.a_con,
                    option_b_label=d.b_label,
                    option_b_pro=d.b_pro,
                    option_b_con=d.b_con,
                    bill_id=bill.id if bill else None,
                    source_note=d.source,
                    status=QuestionStatus.초안,
                    created_by=QuestionCreator.admin,
                )
            )
            n_new += 1

        db.commit()
        print(
            f"문항 초안 시드(장단점 Ⓐ/Ⓑ): 신규 {n_new}, 기존건너뜀 {n_skip}, "
            f"의안미매핑 {n_no_bill}. 모두 status=초안"
        )
    finally:
        db.close()


if __name__ == "__main__":
    run()
